        <h1><b>What is PCOS?</b></h1>
        <p>Polycystic Ovary Syndrome (PCOS) is a hormonal disorder that affects the ovaries, which are the female reproductive organs responsible for producing eggs. It's a complex condition that affects women of childbearing age, typically between 15 and 44 years old.</p>

        <h2><b>What Happens in PCOS?</b></h2>
        <p>PCOS is characterized by a group of symptoms affecting the ovaries and ovulation. The three primary features of PCOS are:</p>
        <ol>
            <li><b>Ovarian Cysts:</b> Numerous small, fluid-filled sacs form in the ovaries.</li>
            <li><b>Elevated Male Hormones:</b> Higher levels of androgens can lead to symptoms like facial hair and acne.</li>
            <li><b>Irregular or Missed Periods:</b> Hormonal imbalances often cause irregular menstrual cycles.</li>
        </ol>

        <h1><b>Symptoms of PCOS</b></h1>
        <img src = "symptoms.jpg" atl = "Description of image" width = "450" height="450">
        <p>Common symptoms of PCOS include:</p>
        <ul>
            <li><b>Irregular periods:</b> Infrequent or prolonged menstrual cycles.</li>
            <li><b>Acne:</b> Pimples and oily skin.</li>
            <li><b>Excess hair growth:</b> Unwanted hair on the face and body.</li>
            <li><b>Weight gain:</b> Many women experience weight gain.</li>
            <li><b>Cysts on ovaries:</b> Detected by ultrasound.</li>
            <li><b>Fertility issues:</b> Difficulty getting pregnant.</li>
            <li><b>Mood changes:</b> Depression and anxiety.</li>
            <li><b>Sleep apnea:</b> Pauses in breathing during sleep.</li>
            <li><b>Headaches:</b> Frequent headaches due to hormonal fluctuations.</li>
        </ul>

        <h1><b>Causes of PCOS</b></h1>
        <img src = "causes.jpg" atl = "Description of image" width = "450" height="450">
        <p>The exact cause of PCOS isn't known, but factors thought to contribute include:</p>
        <ul>
            <li><b>Excess weight:</b> Weight gain plays a role in the development of PCOS.</li>
            <li><b>High levels of androgens:</b> Hyperandrogenism affects most people with PCOS.</li>
            <li><b>Insulin resistance:</b> Extra insulin can affect ovulation.</li>
            <li><b>Low-grade inflammation:</b> Can cause the body to produce excess androgens.</li>
        </ul>

