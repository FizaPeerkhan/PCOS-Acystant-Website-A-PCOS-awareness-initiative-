        <h1><b>DIAGNOSIS:</b></h1>
        <p>You may be diagnosed with PCOS if you have at least two of the following symptoms:</p>
        <ul>
            <li>Irregular periods</li>
            <li>Signs of excess androgens</li>
            <li>Ovarian cysts visible on an ultrasound</li>
        </ul>

        <p><b>How to get tested for PCOS:</b> Your doctor may start with a physical exam, followed by tests to check hormone levels.</p>

        <h1><b>Treatments for PCOS</b></h1>
        <img src = "treatment.jpg" atl = "Description of image" width = "450" height="450">
        <p>Various treatments can help manage symptoms:</p>
        <ul>
            <li><b>Lifestyle changes:</b> Diet and exercise can help manage weight and symptoms.</li>
            <li><b>Medications:</b> Hormonal birth control can help regulate periods, while medications like Metformin can improve insulin sensitivity.</li>
            <li><b>Fertility treatments:</b> Medications like Clomiphene can help induce ovulation for those trying to conceive.</li>
            <li><b>Hair removal therapies:</b> Methods like laser hair removal can be effective for excess hair growth.</li>
        </ul>
